package ex1;

/**
 * Cette classe fournit des méthodes pour réaliser des opérations arithmétiques de base.
 */
public class Calcul {

	/**
	 * Additionne deux entiers donnés.
	 * @param a le premier entier
	 * @param b le deuxième entier
	 * @return la somme des deux entiers
	 */
	public int somme(int a, int b) {
		return a + b;
	}

	/**
	 * Soustrait deux entiers donnés.
	 * @param a le premier entier
	 * @param b le deuxième entier
	 * @return le résultat de la soustraction
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}
